package Entidade;

import java.util.Date;

/**
 *
 * @author SCC4
 */
public class PrecoServAdicional {

    private int id;
    private String sigla;
    private String desc;
    private float valor;
    private Date data;
    private String tipo;

    public PrecoServAdicional(int id, String sigla, String desc, float valor, Date data, String tipo) {
        this.id = id;
        this.sigla = sigla;
        this.desc = desc;
        this.valor = valor;
        this.data = data;
        this.tipo = tipo;
    }

    public Date getData() {
        return data;
    }

    public void setData(Date data) {
        this.data = data;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSigla() {
        return sigla;
    }

    public void setSigla(String sigla) {
        this.sigla = sigla;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public float getValor() {
        return valor;
    }

    public void setValor(float valor) {
        this.valor = valor;
    }

}
