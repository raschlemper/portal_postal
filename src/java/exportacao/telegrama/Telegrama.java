package exportacao.telegrama;

import java.util.Date;

public class Telegrama {
    private long id;
    private ItemTelegrama remetente;
    private ItemTelegrama destinatario;
    private String texto;
    private String copiaTelegrama;
    private String pedidoConfirmacao;
    private double valor;
    private String etiqueta;
    private TelegramaStatus status;
    private Date dataEnvio;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public ItemTelegrama getRemetente() {
        return remetente;
    }

    public void setRemetente(ItemTelegrama remetente) {
        this.remetente = remetente;
    }

    public ItemTelegrama getDestinatario() {
        return destinatario;
    }

    public void setDestinatario(ItemTelegrama destinatario) {
        this.destinatario = destinatario;
    }

    public String getTexto() {
        return texto;
    }

    public void setTexto(String texto) {
        this.texto = texto;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }

    public String getEtiqueta() {
        return etiqueta;
    }

    public void setEtiqueta(String etiqueta) {
        this.etiqueta = etiqueta;
    }

    public TelegramaStatus getStatus() {
        return status;
    }

    public void setStatus(TelegramaStatus status) {
        this.status = status;
    }

    public String getCopiaTelegrama() {
        return copiaTelegrama;
    }

    public void setCopiaTelegrama(String copiaTelegrama) {
        this.copiaTelegrama = copiaTelegrama;
    }

    public String getPedidoConfirmacao() {
        return pedidoConfirmacao;
    }

    public void setPedidoConfirmacao(String pedidoConfirmacao) {
        this.pedidoConfirmacao = pedidoConfirmacao;
    }

    public Date getDataEnvio() {
        return dataEnvio;
    }

    public void setDataEnvio(Date dataEnvio) {
        this.dataEnvio = dataEnvio;
    }
    
}
