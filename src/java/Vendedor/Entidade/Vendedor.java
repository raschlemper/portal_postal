package Vendedor.Entidade;

/**
 *
 * @author Fernando
 */
public class Vendedor {
    private int idVendedor;
    private String nomeVendedor;
    private String refencia;
    private int listaClientes;
    private int ativo;

    public Vendedor(int idVendedor, String nomeVendedor, String refencia, int listaClientes) {
        this.idVendedor = idVendedor;
        this.nomeVendedor = nomeVendedor;
        this.refencia = refencia;
        this.listaClientes = listaClientes;
    }

    
    public int getIdVendedor() {
        return idVendedor;
    }

    public void setIdVendedor(int idVendedor) {
        this.idVendedor = idVendedor;
    }

    public String getNomeVendedor() {
        return nomeVendedor;
    }

    public void setNomeVendedor(String nomeVendedor) {
        this.nomeVendedor = nomeVendedor;
    }

    public String getRefencia() {
        return refencia;
    }

    public void setRefencia(String refencia) {
        this.refencia = refencia;
    }

    public int getListaClientes() {
        return listaClientes;
    }

    public void setListaClientes(int listaClientes) {
        this.listaClientes = listaClientes;
    }

    public int getAtivo() {
        return ativo;
    }

    public void setAtivo(int ativo) {
        this.ativo = ativo;
    }
    
    
    
}
