package Entidade;

/**
 *
 * @author Fernando
 */
public class SRO {

    private int idSRO;
    private int status;
    private String tipo;
    private String descricao;
    private int agrupamento;

    public SRO(int idSRO, int status, String tipo, String descricao, int agrupamento) {
        this.idSRO = idSRO;
        this.status = status;
        this.tipo = tipo;
        this.descricao = descricao;
        this.agrupamento = agrupamento;
    }
    

    public int getIdSRO() {
        return idSRO;
    }

    public void setIdSRO(int idSRO) {
        this.idSRO = idSRO;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public int getAgrupamento() {
        return agrupamento;
    }

    public void setAgrupamento(int agrupamento) {
        this.agrupamento = agrupamento;
    }
    
    
}
