package Entidade;

/**
 *
 * @author SCC4
 */
public class Amarracao {

    private int idAmarracao;
    private String nomeAmarracao;
    private String siglaAmarracao;
    private int ativo;
    private String cepCT;
    private String nomeCT;

    public Amarracao(int idAmarracao, String nomeAmarracao, String siglaAmarracao, int ativo, String cepCT, String nomeCT) {
        this.idAmarracao = idAmarracao;
        this.nomeAmarracao = nomeAmarracao;
        this.siglaAmarracao = siglaAmarracao;
        this.ativo = ativo;
        this.cepCT = cepCT;
        this.nomeCT = nomeCT;
    }

    public String getCepCT() {
        return cepCT;
    }

    public void setCepCT(String cepCT) {
        this.cepCT = cepCT;
    }

    public String getNomeCT() {
        return nomeCT;
    }

    public void setNomeCT(String nomeCT) {
        this.nomeCT = nomeCT;
    }
    
    public int getAtivo() {
        return ativo;
    }

    public void setAtivo(int ativo) {
        this.ativo = ativo;
    }

    public int getIdAmarracao() {
        return idAmarracao;
    }

    public void setIdAmarracao(int idAmarracao) {
        this.idAmarracao = idAmarracao;
    }

    public String getNomeAmarracao() {
        return nomeAmarracao;
    }

    public void setNomeAmarracao(String nomeAmarracao) {
        this.nomeAmarracao = nomeAmarracao;
    }

    public String getSiglaAmarracao() {
        return siglaAmarracao;
    }

    public void setSiglaAmarracao(String siglaAmarracao) {
        this.siglaAmarracao = siglaAmarracao;
    }

}
