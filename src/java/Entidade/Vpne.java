
package Entidade;


public class Vpne {

    private String sro;
    private String descricao;
    private String valor;
    private int idCliente;
    private int idDepartamento;
    private String nomeDepto;
    private String data;

    private Destinatario remVpne;
    private Destinatario destVpne;

    public Vpne(String sro, String descricao, String valor, int idCliente, int idDepartamento, String nomeDepto, String data, Destinatario remVpne, Destinatario destVpne) {
        this.sro = sro;
        this.descricao = descricao;
        this.valor = valor;
        this.idCliente = idCliente;
        this.idDepartamento = idDepartamento;
        this.nomeDepto = nomeDepto;
        this.data = data;
        this.remVpne = remVpne;
        this.destVpne = destVpne;
    }

    public String getNomeDepto() {
        return nomeDepto;
    }

    public void setNomeDepto(String nomeDepto) {
        this.nomeDepto = nomeDepto;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public int getIdCliente() {
        return idCliente;
    }

    public void setIdCliente(int idCliente) {
        this.idCliente = idCliente;
    }

    public int getIdDepartamento() {
        return idDepartamento;
    }

    public void setIdDepartamento(int idDepartamento) {
        this.idDepartamento = idDepartamento;
    }

    public String getSro() {
        return sro;
    }

    public void setSro(String sro) {
        this.sro = sro;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }

    public Destinatario getRemVpne() {
        return remVpne;
    }

    public void setRemVpne(Destinatario remVpne) {
        this.remVpne = remVpne;
    }

    public Destinatario getDestVpne() {
        return destVpne;
    }

    public void setDestVpne(Destinatario destVpne) {
        this.destVpne = destVpne;
    }

}
