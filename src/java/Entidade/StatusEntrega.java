package Entidade;

/**
 *
 * @author RICADINHO
 */
public class StatusEntrega {

    private int id;
    private String nome;
    private int isCancelar;

    public StatusEntrega(int id, String nome, int isCancelar) {
        this.id = id;
        this.nome = nome;
        this.isCancelar = isCancelar;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIsCancelar() {
        return isCancelar;
    }

    public void setIsCancelar(int isCancelar) {
        this.isCancelar = isCancelar;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

}
