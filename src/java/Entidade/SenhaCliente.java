package Entidade;

/**
 *
 * @author Administrador
 */
public class SenhaCliente {

    private int id;
    private int codigo;
    private String senha;
    private String login;
    private int nivel;
    private String acessos;
    private String departamentos;
    private String servicos;

    public SenhaCliente(int id, int codigo, String senha, String login, int nivel, String acessos, String departamentos, String servicos) {
        this.id = id;
        this.codigo = codigo;
        this.senha = senha;
        this.login = login;
        this.nivel = nivel;
        this.acessos = acessos;
        this.departamentos = departamentos;
        this.servicos = servicos;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAcessos() {
        return acessos;
    }

    public void setAcessos(String acessos) {
        this.acessos = acessos;
    }

    public String getDepartamentos() {
        return departamentos;
    }

    public void setDepartamentos(String departamentos) {
        this.departamentos = departamentos;
    }

    public String getServicos() {
        return servicos;
    }

    public void setServicos(String servicos) {
        this.servicos = servicos;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public int getNivel() {
        return nivel;
    }

    public void setNivel(int nivel) {
        this.nivel = nivel;
    }

}
