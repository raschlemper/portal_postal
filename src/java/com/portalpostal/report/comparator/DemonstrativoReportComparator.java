package com.portalpostal.report.comparator;

import com.portalpostal.report.dto.DemonstrativoReportDTO;
import java.util.Comparator;

public class DemonstrativoReportComparator implements Comparator<DemonstrativoReportDTO> {

    @Override
    public int compare(DemonstrativoReportDTO t, DemonstrativoReportDTO t1) {
        return t.getDescricao().compareTo(t1.getDescricao());
    }
    
}
