package com.portalpostal.model;

import com.portalpostal.model.dd.TipoConta;

public class InformacaoBancaria {
    
    private Integer idInformacaoBancaria;
    private Colaborador colaborador;
    private TipoConta tipoConta;
    private Banco banco;
    private Integer agencia;
    private Integer agenciaDv;
    private Integer contaCorrente;
    private Integer contaCorrenteDv;

    public Integer getIdInformacaoBancaria() {
        return idInformacaoBancaria;
    }

    public void setIdInformacaoBancaria(Integer idInformacaoBancaria) {
        this.idInformacaoBancaria = idInformacaoBancaria;
    }

    public Colaborador getColaborador() {
        return colaborador;
    }

    public void setColaborador(Colaborador colaborador) {
        this.colaborador = colaborador;
    }

    public TipoConta getTipoConta() {
        return tipoConta;
    }

    public void setTipoConta(TipoConta tipoConta) {
        this.tipoConta = tipoConta;
    }

    public Banco getBanco() {
        return banco;
    }

    public void setBanco(Banco banco) {
        this.banco = banco;
    }

    public Integer getAgencia() {
        return agencia;
    }

    public void setAgencia(Integer agencia) {
        this.agencia = agencia;
    }

    public Integer getAgenciaDv() {
        return agenciaDv;
    }

    public void setAgenciaDv(Integer agenciaDv) {
        this.agenciaDv = agenciaDv;
    }

    public Integer getContaCorrente() {
        return contaCorrente;
    }

    public void setContaCorrente(Integer contaCorrente) {
        this.contaCorrente = contaCorrente;
    }

    public Integer getContaCorrenteDv() {
        return contaCorrenteDv;
    }

    public void setContaCorrenteDv(Integer contaCorrenteDv) {
        this.contaCorrenteDv = contaCorrenteDv;
    }
    
}
