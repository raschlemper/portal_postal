package com.portalpostal.model;

import java.util.List;
import java.util.Map;

public class CentroCusto {
    
    private Integer idCentroCusto;
    private Integer codigo;
    private String nome;
    private String descricao;
    private Integer nivel;
    private Map<Integer,Integer> estrutura;
    private CentroCusto grupo;
    private List<CentroCusto> centros;
    private List<Lancamento> lancamentos;
    private List<LancamentoProgramado> lancamentosProgramados;

    public Integer getIdCentroCusto() {
        return idCentroCusto;
    }

    public void setIdCentroCusto(Integer idCentroCusto) {
        this.idCentroCusto = idCentroCusto;
    }   

    public Integer getCodigo() {
        return codigo;
    }

    public void setCodigo(Integer codigo) {
        this.codigo = codigo;
    } 

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public Integer getNivel() {
        return nivel;
    }

    public void setNivel(Integer nivel) {
        this.nivel = nivel;
    }

    public Map<Integer, Integer> getEstrutura() {
        return estrutura;
    }

    public void setEstrutura(Map<Integer, Integer> estrutura) {
        this.estrutura = estrutura;
    }  

    public CentroCusto getGrupo() {
        return grupo;
    }

    public void setGrupo(CentroCusto grupo) {
        this.grupo = grupo;
    }

    public List<CentroCusto> getCentros() {
        return centros;
    }

    public void setCentros(List<CentroCusto> centros) {
        this.centros = centros;
    }

    public List<Lancamento> getLancamentos() {
        return lancamentos;
    }

    public void setLancamentos(List<Lancamento> lancamentos) {
        this.lancamentos = lancamentos;
    }

    public List<LancamentoProgramado> getLancamentosProgramados() {
        return lancamentosProgramados;
    }

    public void setLancamentosProgramados(List<LancamentoProgramado> lancamentosProgramados) {
        this.lancamentosProgramados = lancamentosProgramados;
    }
    
}
