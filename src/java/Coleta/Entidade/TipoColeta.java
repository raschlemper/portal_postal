package Coleta.Entidade;

/**
 *
 * @author SCC4
 */
public class TipoColeta {
    
    private int idTipoColeta;
    private String tipo;

    public TipoColeta(int idTipoColeta, String tipo) {
        this.idTipoColeta = idTipoColeta;
        this.tipo = tipo;
    }

    public int getIdTipoColeta() {
        return idTipoColeta;
    }

    public void setIdTipoColeta(int idTipoColeta) {
        this.idTipoColeta = idTipoColeta;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

}
