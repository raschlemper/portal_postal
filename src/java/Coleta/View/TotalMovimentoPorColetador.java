package Coleta.View;

public class TotalMovimentoPorColetador {

    private String idColetador;
    private String nomeColetador;
    private Double total;

    public String getIdColetador() {
        return idColetador;
    }

    public void setIdColetador(String idColetador) {
        this.idColetador = idColetador;
    }

    public String getNomeColetador() {
        return nomeColetador;
    }

    public void setNomeColetador(String nomeColetador) {
        this.nomeColetador = nomeColetador;
    }

    public Double getTotal() {
        return total;
    }

    public void setTotal(Double total) {
        this.total = total;
    }

}
